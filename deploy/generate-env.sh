#!/bin/sh

set -eu

script_dir=$(CDPATH= cd -- "$(dirname -- "$0")" && pwd)
env_file=${1:-"$script_dir/.env"}

if [ "$#" -gt 1 ]; then
    echo "usage: $0 [output-file]" >&2
    exit 2
fi

if [ -e "$env_file" ]; then
    echo "refusing to overwrite existing $env_file" >&2
    exit 1
fi

if ! command -v openssl >/dev/null 2>&1; then
    echo "openssl is required to generate a development password" >&2
    exit 1
fi

umask 077
tmp_file=$(mktemp "${env_file}.tmp.XXXXXX")
trap 'rm -f -- "$tmp_file"' EXIT HUP INT TERM

password=$(openssl rand -hex 32)
{
    echo "# Generated by deploy/generate-env.sh; do not commit this file."
    echo "OPEN_SPLUNK_CLICKHOUSE_PASSWORD=$password"
} >"$tmp_file"
mv -- "$tmp_file" "$env_file"
trap - EXIT HUP INT TERM

echo "wrote $env_file with permissions restricted by umask 077"
